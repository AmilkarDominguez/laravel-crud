    file.write('                        clip-rule="evenodd" />\n')
    file.write('                </svg>\n')
    file.write('            </a>\n')
    file.write('        </div>\n')
    file.write('        <div class="m-5">\n')
    file.write('            <livewire:'+nameView+'.'+nameView+'-data-table />\n')
    file.write('        </div>\n')
    file.write('    </div>\n')
    file.write('</div>\n')
    file.close()
