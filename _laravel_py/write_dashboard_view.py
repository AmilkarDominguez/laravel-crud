    file.write('        <div class="font-semibold text-xl text-gray-800 leading-tight">\n')
    file.write('            '+nombre+'\n')
    file.write('        </div>\n')
    file.write('    </x-slot>\n')
    file.write('    <div class="max-w-7xl mx-auto py-10 sm:px-6 lg:px-8">\n')
    file.write('        <div class="w-full flex justify-end space-x-2">\n')
